#' Kongsfjorden food web: nodes
#'
#' Food web including metazoan parasites for an intertidal ecosystem in New
#' Zealand.
#'
#' @details The data set is included here "as is". In this network, nodes are
#'   species or taxonomical groups (e.g., Phytoplankton) and binary links
#'   represent feeding relationships. Kongsfjorden is a glacial fjord on the
#'   northwest corner of the Svalbard archipelago. It is a 30 km open fjord with
#'   no marked sill at the entrance, and with a maximum depth exceeding 300 m.
#'   The network consists of 262 species with 1,544 feeding interactions.
#'
